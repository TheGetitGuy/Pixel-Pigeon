/**
 * Used internally for collisions and overlapping
 */
export interface Rectangle {
  /**
   * The height of the Rectangle
   */
  height: number;
  /**
   * The width of the Rectangle
   */
  width: number;
  /**
   * The X position of the Rectangle
   */
  x: number;
  /**
   * The Y position of the Rectangle
   */
  y: number;
}
